package Objetos.Buscar;

public class ModeloBuscar {
    private double tempoDeExecucao;
    private int tamanhoDoConjuntoDeDados;
    private int numComparacoes;
    private int alturaArvore;
    private int tamanhoDaArvore;
	
    public double getTempoDeExecucao() {
		return tempoDeExecucao;
	}
	
	public void setTempoDeExecucao(double tempoDeExecucao) {
		this.tempoDeExecucao = tempoDeExecucao;
	}
	
	public int getTamanhoDoConjuntoDeDados() {
		return tamanhoDoConjuntoDeDados;
	}
	
	public void setTamanhoDoConjuntoDeDados(int tamanhoDoConjuntoDeDados) {
		this.tamanhoDoConjuntoDeDados = tamanhoDoConjuntoDeDados;
	}
	
	public int getNumComparacoes() {
		return numComparacoes;
	}
	
	public void setNumComparacoes(int numComparacoes) {
		this.numComparacoes = numComparacoes;
	}
	
	public int getAlturaArvore() {
		return alturaArvore;
	}
	
	public void setAlturaArvore(int alturaArvore) {
		this.alturaArvore = alturaArvore;
	}

	public int getTamanhoDaArvore() {
		return tamanhoDaArvore;
	}

	public void setTamanhoDaArvore(int tamanhoDaArvore) {
		this.tamanhoDaArvore = tamanhoDaArvore;
	}

}
